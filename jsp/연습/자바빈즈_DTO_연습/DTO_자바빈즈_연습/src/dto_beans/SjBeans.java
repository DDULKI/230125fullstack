package dto_beans;

/**
 * SjBeans
 */
public class SjBeans {
    String name;
    int age;
    int mat;
    int kor;
    int sing;

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return this.age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    int eng;

    public int getEng() {
        return this.eng;
    }

    public void setEng(int eng) {
        this.eng = eng;
    }

    public int getMat() {
        return this.mat;
    }

    public void setMat(int mat) {
        this.mat = mat;
    }

    public int getKor() {
        return this.kor;
    }

    public void setKor(int kor) {
        this.kor = kor;
    }
    
    public int getSing() {
        return this.sing;
    }

    public void setSing(int sing) {
        this.sing = sing;
    }
    
    
}