package kurly;

/**
 * UserDTO
 */
public class UserDTO {

    private int idx;
    private String id;
    private String pw;
    private String name;
    private String email;
    private String hp;
    private String addr;
    private String gender;
    private String birth;
    private String chooga;
    private String service;
    private String gaib_date;

    public int getIdx() {
        return this.idx;
    }

    public void setIdx(int idx) {
        this.idx = idx;
    }

    public String getId() {
        return this.id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPw() {
        return this.pw;
    }

    public void setPw(String pw) {
        this.pw = pw;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return this.email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getHp() {
        return this.hp;
    }

    public void setHp(String hp) {
        this.hp = hp;
    }

    public String getAddr() {
        return this.addr;
    }

    public void setAddr(String addr) {
        this.addr = addr;
    }

    public String getGender() {
        return this.gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getBirth() {
        return this.birth;
    }

    public void setBirth(String birth) {
        this.birth = birth;
    }

    public String getChooga() {
        return this.chooga;
    }

    public void setChooga(String chooga) {
        this.chooga = chooga;
    }

    public String getService() {
        return this.service;
    }

    public void setService(String service) {
        this.service = service;
    }

    public String getGaib_date() {
        return this.gaib_date;
    }

    public void setGaib_date(String gaib_date) {
        this.gaib_date = gaib_date;
    }
    
    
}